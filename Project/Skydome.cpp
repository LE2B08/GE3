#include "Skydome.h"


/// -------------------------------------------------------------
///							初期化処理
/// -------------------------------------------------------------
void Skydome::Initialize()
{
	object3D_ = std::make_unique<Object3D>();
	object3D_->Initialize("Skydome/skydome.gltf");
	object3D_->SetScale({ 500.0f,500.0f,500.0f });
	object3D_->SetTranslate({ 0.0f,0.0f,0.0f });
}


/// -------------------------------------------------------------
///							更新処理
/// -------------------------------------------------------------
void Skydome::Update()
{
	object3D_->Update();
}


/// -------------------------------------------------------------
///							描画処理
/// -------------------------------------------------------------
void Skydome::Draw()
{
	object3D_->Draw();
}